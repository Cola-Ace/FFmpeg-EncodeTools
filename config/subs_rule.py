def wash(text: str) -> str:
    """对字幕文本执行完整清洗管线：全角转半角后逐条应用正则替换规则

    Args:
        text: 原始字幕文本

    Returns:
        清洗后的文本
    """
    text = to_half(text)
    for pat, repl in ALL_RULES:
        try:
            # 逐条应用规则
            text = pat.sub(repl, text)
        except Exception:
            pass
    return text
